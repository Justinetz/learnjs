const stepper = [
	{c:1,r:1},
	{c:3,r:1},
	{c:5,r:1},
	{c:7,r:1},
	{c:1,r:2}
];

var calc = function (step) {
	let totalX = 0;
	let row = step.r;
	let column = step.c;
	while (row < height) {
		const rr = parsedArr[row]
		if (rr[column] === '.') {
			console.log('tree', row,column,'O');
		} else {
			console.log('tree', row,column,'X');
			totalX++;
		}
		row += step.r;
		column += step.c;
		if (column >= width) {
			column = column - width
		}
	}
	return totalX;
};

const vals = new Array();
let multipl = 1;
stepper.forEach(s => {
	const v = calc(s);
	vals.push(v);
	multipl *= v;
});
console.log(vals.join(','));
